<h4>4. Arrival, Accommodation Occupancy, Departure:</h4>

<p>Key handover takes place at the accommodation upon arrival, and two or three sets of keys will be provided as needed.</p>

<p>During the peak season, the accommodation can be occupied on Saturdays between 3:00 PM and 6:00 PM, and outside the peak season at a pre-arranged time. If you are unable to arrive at the specified time due to unexpected difficulties (traffic jam, car breakdown, late departure), please notify the operators immediately at the emergency phone number 06/70 546-5236.</p>

<p>The accommodation must be vacated by 10:00 AM on the day of departure.</p>



<h4>5. Additional Costs:</h4>

<p><b><u>Deposit:</u></b> A deposit must be paid in cash when receiving the key to the guesthouse.</p>

<ul> <li>The deposit amount is 50,000 € for the booked period.</li> </ul>

<p>If no damage occurs, the deposit amount will be refunded in cash upon departure. The guest will receive the deposit back when returning the key, provided no damage has occurred in the guesthouse. If the key is lost, the deposit amount will not be refunded. If the guest causes damage, they are obliged to compensate for it at the current market value, which will be deducted from the deposit. If the damage exceeds the deposit amount, compensation will be paid according to an agreement with the operators.</p>

<p><b>Tourist Tax:</b> The tourist tax amount in 2024 is <b>500 €/person/night</b>, which must be paid by every guest over 18 years old. The tax must be paid in cash to the operators.</p>

<h4>6. Cancellation Policy:</h4>

<p>The ordered guesthouse service can be canceled in writing.</p> <p>The cancellation fees are as follows:</p>

<ul> <li>Free cancellation within 36 hours of sending the invoice, and on or before the 30th day before the start of the holiday.</li> <li>Between 29-14 days before the start of the holiday: 50% of the deposit amount.</li> <li>Between 0-13 days before the start of the holiday: 100% of the deposit amount.</li> </ul>

<p>In case of cancellation, the operators will refund the amount reduced by the cancellation fee within 5 working days of receiving the cancellation. An exception to this is if the booker cannot participate in the holiday due to a medical condition or hospital-confirmed illness. In this case, if the booker notifies and proves this in writing, the operators will refund the full deposit amount by the 7th day before the start of the holiday.</p>

<p>If the deposit was paid by SZÉP card, in case of cancellation, we can only refund the amount reduced by the current bank commission.</p>

<h4>7. Modification of the Order by the Customer:</h4>

<p>Booking modifications can be made in writing based on prior agreement.</p>

<ul> <li>Modifications to the number of guests can be made until the day before arrival.</li> <li>Modifications to the date of an already finalized and confirmed booking can be made once, depending on availability. In this case, the paid deposit can be used within 1 year from the original arrival date.</li> </ul>

<p>In this case, the modified confirmation becomes valid.</p>

<h4>8. Modification by R&R Guesthouse:</h4>

<p>The operators of R&R Guesthouse reserve the right to make modifications. Unforeseen technical problems (pipe burst, water-sewer-electricity technical problems, and other unexpected, unforeseeable tragedies, natural disasters, damages caused by previous guests, etc.) may occur, which could prevent the holiday.</p> <p>The operators of R&R Guesthouse will notify the customer immediately upon learning of the problem, and if the problem cannot be resolved by the arrival date, they will offer another date with price adjustment. If this or the offered date is not suitable for the customer, the operators will refund the full deposit amount to the customer.</p>

<h4>9. Parking:</h4>

<p>We ask our guests to use the following areas for parking:</p>

<ul> <li>2 parking spaces within the garden</li> <li>2 parking spaces in front of the guesthouse fence for street parking</li> </ul> <p>We do not recommend parking on the opposite side of the street or in neighboring areas.</p>

