<h4>3. Booking, Deposit, and Payment of Accommodation Fee:</h4> <p>Booking can only be initiated in writing, either by email or by filling out the form available on the website. When booking, you must provide the name of the booker, address, email address, phone number, number of guests, and the arrival and departure dates. (Please indicate in the comments section if you need any baby-friendly equipment.) A€er receiving your booking request, we will confirm the order within 24 hours, thereby acknowledging the reservation of the guesthouse. The booking becomes final once the booker pays the deposit indicated on the invoice sent by the operators following the confirmation.</p> <p>The deposit is due within 72 hours of sending the invoice. It can be paid by bank transfer or by SZÉP card transfer. The operators of the R&R Guesthouse will inform the booker via email upon receipt of the amount. If the booker fails to pay the deposit within the deadline indicated in the confirmation, the booking will be canceled.</p>

<p>The deposit is a percentage of the total accommodation fee:</p>

<ul> <li>30% if the booking is made more than 14 days before the start of the holiday,</li> <li>75% if the booking is made within 14 days before the start of the holiday.</li> </ul>

<p>(The total accommodation fee is the product of the number of booked days and the daily rate of the house.)</p>

<p>A€er sending the booking confirmation, the booker is obligated to pay the operators of the R&R Guesthouse until the booking is canceled in writing. If you decide to shorten your stay during the holiday, we are unable to refund the remaining accommodation fee.</p>

<p>The deposit can be paid in Hungarian Forints. The full accommodation fee is due no later than 3 days before the holiday, by bank transfer or SZÉP card transfer.</p>

